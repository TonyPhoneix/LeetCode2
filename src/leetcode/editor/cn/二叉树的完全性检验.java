        public boolean isCompleteTree(TreeNode root) {
            //å…³é”®ï¼Œlinkedlistå¯ä»¥ä¿å­˜null
            Queue<TreeNode> q = new LinkedList<>();
            q.offer(root);
            TreeNode pre = root;
            while (!q.isEmpty()) {
                TreeNode poll = q.poll();
                if (pre == null && poll != null) {
                    return false;
                }
                if (poll != null) {
                    q.offer(poll.left);
                    q.offer(poll.right);
                }
                pre = poll;
            }
            return true;
        }
